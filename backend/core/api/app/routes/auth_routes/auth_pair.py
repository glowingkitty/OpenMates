# backend/core/api/app/routes/auth_routes/auth_pair.py
# Magic Pair Login — cross-device authentication via QR code or short typed token.
#
# Architecture: docs/architecture/device-sessions.md (Device Authorization Methods § Magic Login Links)
# Security model: Zero-knowledge — server stores only AES-encrypted bundle; PIN never decrypted server-side.
#
# Full flow (two devices):
#   1. [Initiating device]  POST /pair/initiate   → gets 6-char token + URL
#   2. [Logged-in device]   GET  /pair/info/{tok} → sees who wants to pair (device info)
#   3. [Logged-in device]   POST /pair/authorize/{tok} → uploads AES-encrypted bundle + 6-digit PIN
#   4. [Initiating device]  GET  /pair/poll/{tok} → learns bundle is "ready"
#   5. [Initiating device]  POST /pair/complete/{tok} → sends PIN → gets encrypted bundle back
#   6. [Initiating device]  Client decrypts bundle with PIN → recovers lookup_hash
#   7. [Initiating device]  POST /auth/login with recovered lookup_hash → normal session
#
# Redis key TTLs (all scoped to pair: namespace, no overlap with existing keys):
#   pair:req:{token}      5 min   — request metadata + status
#   pair:bundle:{token}   2 min   — encrypted bundle (set by /authorize, consumed by /complete)
#   pair:attempts:{token} 2 min   — integer attempt counter for brute-force guard

from __future__ import annotations

import hashlib
import logging
import re
import secrets
import time
from typing import Optional

from fastapi import APIRouter, Depends, HTTPException, Request
from pydantic import BaseModel, field_validator

from backend.core.api.app.models.user import User
from backend.core.api.app.routes.auth_routes.auth_dependencies import (
    get_cache_service,
    get_compliance_service,
    get_current_user,
    get_directus_service,
)
from backend.core.api.app.services.cache import CacheService
from backend.core.api.app.services.compliance import ComplianceService
from backend.core.api.app.services.directus import DirectusService
from backend.core.api.app.services.limiter import limiter
from backend.core.api.app.utils.device_fingerprint import (
    _extract_client_ip,
    derive_device_name,
    get_geo_data_from_ip,
    truncate_ip,
)

logger = logging.getLogger(__name__)
router = APIRouter()

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Unambiguous uppercase alphabet — removes chars confused visually (0/O, 1/I, 5/S, 2/Z)
_PAIR_TOKEN_ALPHABET = "ABCDEFGHJKLMNPQRTUVWXY3468"
_PAIR_TOKEN_LENGTH = 6

_PAIR_REQUEST_TTL = 300    # 5 min — QR / URL validity window
_PAIR_BUNDLE_TTL = 120     # 2 min — encrypted bundle sits in Redis
_MAX_PIN_ATTEMPTS = 5      # Brute-force guard; exceeded → token destroyed

_REDIS_REQ_PREFIX = "pair:req:"
_REDIS_BUNDLE_PREFIX = "pair:bundle:"
_REDIS_ATTEMPTS_PREFIX = "pair:attempts:"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _generate_token() -> str:
    """Generate a cryptographically random 6-char pair token from unambiguous alphabet."""
    return "".join(secrets.choice(_PAIR_TOKEN_ALPHABET) for _ in range(_PAIR_TOKEN_LENGTH))


async def _cleanup_pair_keys(cache_service: CacheService, token: str) -> None:
    """Atomically delete all Redis keys for a pairing session."""
    await cache_service.delete(f"{_REDIS_REQ_PREFIX}{token}")
    await cache_service.delete(f"{_REDIS_BUNDLE_PREFIX}{token}")
    await cache_service.delete(f"{_REDIS_ATTEMPTS_PREFIX}{token}")


# ---------------------------------------------------------------------------
# Request / Response schemas
# ---------------------------------------------------------------------------

class PairInitiateRequest(BaseModel):
    """Optional: human-readable device hint displayed on the authorizing device."""
    device_hint: Optional[str] = None


class PairInitiateResponse(BaseModel):
    token: str        # 6-char uppercase token
    expires_in: int   # Seconds until token expires


class PairAuthorizeRequest(BaseModel):
    """
    Sent by the logged-in (authorizing) device after the user approves the pairing.
    The bundle is AES-256-GCM encrypted client-side with a PIN-derived key.
    The server stores the opaque blob; it cannot decrypt it.
    """
    encrypted_bundle: str           # Base64(ciphertext) — AES-GCM
    iv: str                         # Base64 12-byte IV
    pin: str                        # 6-char alphanumeric PIN generated by client (shown to user, not stored in DB)
    authorizer_device_name: Optional[str] = None
    auto_logout_minutes: Optional[int] = None  # Session lifetime chosen by the authorizing device

    @field_validator("pin")
    @classmethod
    def validate_pin(cls, v: str) -> str:
        normalized = v.upper()
        if not re.fullmatch(r"[A-Z0-9]{6}", normalized):
            raise ValueError("pin must be exactly 6 alphanumeric characters")
        return normalized


class PairAuthorizeResponse(BaseModel):
    success: bool


class PairPollResponse(BaseModel):
    status: str                              # "waiting" | "ready" | "expired"
    authorizer_device_name: Optional[str] = None  # Only when status == "ready"


class PairCredentialsResponse(BaseModel):
    """Authenticated credentials payload used to build pair-login encrypted bundle."""
    lookup_hash: str
    user_email_salt: str
    hashed_email: str  # SHA256(email) — needed by /auth/login for user lookup
    encrypted_key: str
    salt: str


class PairCompleteRequest(BaseModel):
    pin: str  # 6-char alphanumeric PIN entered by user on the initiating device

    @field_validator("pin")
    @classmethod
    def validate_pin(cls, v: str) -> str:
        normalized = v.upper()
        if not re.fullmatch(r"[A-Z0-9]{6}", normalized):
            raise ValueError("pin must be exactly 6 alphanumeric characters")
        return normalized


class PairCompleteResponse(BaseModel):
    """
    Returns the encrypted bundle to the initiating device on success.
    Client decrypts with PIN → recovers {lookup_hash, user_email_salt, …} → calls /auth/login.
    Zero-knowledge: server never sees the decrypted contents.
    """
    success: bool
    encrypted_bundle: Optional[str] = None
    iv: Optional[str] = None
    auto_logout_minutes: Optional[int] = None  # Session lifetime set by the authorizing device
    authorizer_device_name: Optional[str] = None
    message: Optional[str] = None


