#!/usr/bin/env python3
"""Weekly technical-debt scanner for OpenMates.

Produces a durable JSON + Markdown report from read-only source analysis. The
scan is intentionally heuristic: it ranks maintainability risk so the follow-up
OpenCode audit can propose the five most valuable improvement steps.
"""

from __future__ import annotations

import argparse
import ast
import hashlib
import json
import re
import subprocess
from collections import Counter, defaultdict
from dataclasses import asdict, dataclass
from datetime import datetime, timezone
from pathlib import Path
from statistics import median


REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_JSON_REPORT = REPO_ROOT / "logs" / "nightly-reports" / "technical-debt.json"
DEFAULT_MD_REPORT = REPO_ROOT / "logs" / "nightly-reports" / "technical-debt.md"
DEFAULT_STATE_FILE = REPO_ROOT / "scripts" / ".technical-debt-state.json"

SOURCE_EXTENSIONS = {".py", ".ts", ".tsx", ".svelte", ".js", ".mjs", ".css", ".swift"}
CODE_EXTENSIONS = {".py", ".ts", ".tsx", ".svelte", ".js", ".mjs", ".swift"}
TEST_PATH_MARKERS = (".spec.", ".test.", "/tests/", "backend/tests/", "frontend/apps/web_app/tests/")
EXCLUDED_PARTS = {
    ".git",
    ".turbo",
    ".svelte-kit",
    ".vercel",
    ".venv",
    "venv",
    "node_modules",
    "dist",
    "build",
    "coverage",
    "__pycache__",
    "test-results",
    "vaults",
}
GENERATED_MARKERS = ("generated", "do not edit", "auto-generated", "autogenerated")
COMPLEXITY_KEYWORDS = (" if ", " for ", " while ", " case ", " catch ", " except ", " elif ", " && ", " || ", "?")

PATTERNS = {
    "todo": re.compile(r"\b(TODO|FIXME|HACK|XXX)\b", re.IGNORECASE),
    "suppression": re.compile(r"@ts-ignore|@ts-expect-error|eslint-disable|noqa|type:\s*ignore|svelte-ignore"),
    "broad_exception": re.compile(r"except\s+Exception|except\s*:\s*$|catch\s*\([^)]*\)"),
    "silent_failure": re.compile(r"^\s*pass\s*(?:#.*)?$", re.MULTILINE),
    "console_log": re.compile(r"console\.(log|debug|warn|error)|debugger\b|print\("),
    "hardcoded_secret_hint": re.compile(r"(?i)(api[_-]?key|secret|token|password)\s*[:=]\s*['\"][^'\"]{8,}"),
    "store_internal_import": re.compile(r"from\s+['\"][^'\"]*stores/[^'\"]+['\"]"),
    "backend_cross_skill_import": re.compile(r"from\s+backend\.apps\.([^\.]+)\.skills|import\s+backend\.apps\.([^\.]+)\.skills"),
}


@dataclass
class FileMetrics:
    path: str
    ext: str
    lines: int
    code_lines: int
    max_line_length: int
    complexity_signals: int
    function_count: int
    class_count: int
    todo: int
    suppression: int
    broad_exception: int
    silent_failure: int
    console_log: int
    hardcoded_secret_hint: int
    store_internal_import: int
    backend_cross_skill_import: int
    score: float


def _now_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _git_output(args: list[str], timeout: int = 30) -> str:
    result = subprocess.run(["git", *args], cwd=REPO_ROOT, capture_output=True, text=True, timeout=timeout, check=True)
    return result.stdout.strip()


def _git_files() -> list[Path]:
    return [REPO_ROOT / line for line in _git_output(["ls-files"]).splitlines() if line]


def _current_sha() -> str:
    try:
        return _git_output(["rev-parse", "--short", "HEAD"], timeout=10) or "unknown"
    except Exception:
        return "unknown"


def _is_excluded(path: Path) -> bool:
    rel_parts = path.relative_to(REPO_ROOT).parts
    rel = path.as_posix()
    return (
        any(part in EXCLUDED_PARTS for part in rel_parts)
        or "/locales/" in rel
        or rel.endswith(".min.js")
        or rel.endswith(".generated.swift")
    )


def _is_test_file(rel_path: str) -> bool:
    return any(marker in rel_path for marker in TEST_PATH_MARKERS)


def _is_generated(text: str) -> bool:
    head = text[:1200].lower()
    return any(marker in head for marker in GENERATED_MARKERS)


def _count_python_defs(text: str) -> tuple[int, int]:
    try:
        tree = ast.parse(text)
    except SyntaxError:
        return 0, 0
    functions = sum(isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) for node in ast.walk(tree))
    classes = sum(isinstance(node, ast.ClassDef) for node in ast.walk(tree))
    return functions, classes


def _count_generic_defs(text: str) -> tuple[int, int]:
    functions = len(re.findall(r"\b(function\s+\w+|const\s+\w+\s*=\s*(?:async\s*)?\(|async\s+function|=>|def\s+\w+)", text))
    classes = len(re.findall(r"\bclass\s+\w+", text))
    return functions, classes


def _score(raw: dict[str, int]) -> float:
    return round(
        raw["code_lines"] / 80
        + raw["complexity_signals"] * 0.8
        + raw["todo"] * 2
        + raw["suppression"] * 3
        + raw["broad_exception"] * 2.5
        + raw["silent_failure"] * 2
        + raw["console_log"] * 0.8
        + raw["hardcoded_secret_hint"] * 8
        + raw["store_internal_import"] * 6
        + raw["backend_cross_skill_import"] * 6,
        2,
    )


def _analyze_file(path: Path) -> FileMetrics | None:
    if path.suffix not in SOURCE_EXTENSIONS or _is_excluded(path):
        return None
    rel_path = path.relative_to(REPO_ROOT).as_posix()
    if _is_test_file(rel_path):
        return None
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return None
    if _is_generated(text):
        return None

    lines = text.splitlines()
    code_lines = [line for line in lines if line.strip() and not line.strip().startswith(("#", "//", "/*", "*"))]
    complexity_signals = 0
    for line in code_lines:
        padded = f" {line} "
        complexity_signals += sum(padded.count(keyword) for keyword in COMPLEXITY_KEYWORDS)

    functions, classes = _count_python_defs(text) if path.suffix == ".py" else _count_generic_defs(text)
    raw = {
        "code_lines": len(code_lines),
        "complexity_signals": complexity_signals,
        "todo": len(PATTERNS["todo"].findall(text)),
        "suppression": len(PATTERNS["suppression"].findall(text)),
        "broad_exception": len(PATTERNS["broad_exception"].findall(text)),
        "silent_failure": len(PATTERNS["silent_failure"].findall(text)),
        "console_log": len(PATTERNS["console_log"].findall(text)),
        "hardcoded_secret_hint": len(PATTERNS["hardcoded_secret_hint"].findall(text)),
        "store_internal_import": len(PATTERNS["store_internal_import"].findall(text)),
        "backend_cross_skill_import": len(PATTERNS["backend_cross_skill_import"].findall(text)),
    }
    return FileMetrics(
        path=rel_path,
        ext=path.suffix,
        lines=len(lines),
        code_lines=raw["code_lines"],
        max_line_length=max((len(line) for line in lines), default=0),
        complexity_signals=complexity_signals,
        function_count=functions,
        class_count=classes,
        todo=raw["todo"],
        suppression=raw["suppression"],
        broad_exception=raw["broad_exception"],
        silent_failure=raw["silent_failure"],
        console_log=raw["console_log"],
        hardcoded_secret_hint=raw["hardcoded_secret_hint"],
        store_internal_import=raw["store_internal_import"],
        backend_cross_skill_import=raw["backend_cross_skill_import"],
        score=_score(raw),
    )


def _duplication_fingerprints(files: list[Path]) -> list[dict[str, object]]:
    buckets: dict[str, list[tuple[str, int]]] = defaultdict(list)
    for path in files:
        if path.suffix not in CODE_EXTENSIONS or _is_excluded(path):
            continue
        rel_path = path.relative_to(REPO_ROOT).as_posix()
        if _is_test_file(rel_path):
            continue
        try:
            text = path.read_text(encoding="utf-8", errors="replace")
        except OSError:
            continue
        if _is_generated(text):
            continue
        normalized = []
        for line in text.splitlines():
            stripped = re.sub(r"\s+", " ", line.strip())
            if stripped and not stripped.startswith(("#", "//", "/*", "*")):
                normalized.append(stripped)
        for index in range(0, max(0, len(normalized) - 11)):
            block = "\n".join(normalized[index : index + 12])
            if len(block) < 360:
                continue
            digest = hashlib.sha1(block.encode()).hexdigest()[:16]
            buckets[digest].append((rel_path, index + 1))
    duplicates = []
    for digest, locations in buckets.items():
        files_with_match = sorted({location[0] for location in locations})
        if len(files_with_match) >= 2:
            duplicates.append({"fingerprint": digest, "file_count": len(files_with_match), "occurrences": len(locations), "files": files_with_match[:12]})
    return sorted(duplicates, key=lambda item: (item["file_count"], item["occurrences"]), reverse=True)[:40]


def _directory_rollup(metrics: list[FileMetrics]) -> list[dict[str, object]]:
    buckets: dict[str, dict[str, float]] = defaultdict(lambda: {"files": 0, "lines": 0, "score": 0})
    for item in metrics:
        parts = item.path.split("/")
        if parts[0] == "frontend":
            key = "/".join(parts[:4])
        elif parts[0] == "backend":
            key = "/".join(parts[:5])
        else:
            key = parts[0]
        buckets[key]["files"] += 1
        buckets[key]["lines"] += item.lines
        buckets[key]["score"] += item.score
    return [
        {"path": key, "files": int(value["files"]), "lines": int(value["lines"]), "score": round(value["score"], 2)}
        for key, value in sorted(buckets.items(), key=lambda pair: pair[1]["score"], reverse=True)[:30]
    ]


def _git_churn(limit: int) -> list[list[object]]:
    try:
        output = _git_output(["log", "--name-only", "--pretty=format:", "--since=6 months ago"], timeout=60)
    except Exception:
        return []
    counts = Counter(line.strip() for line in output.splitlines() if line.strip())
    return [[path, count] for path, count in counts.most_common(limit)]


def _test_proximity(files: list[Path]) -> dict[str, object]:
    test_files = {path.relative_to(REPO_ROOT).as_posix() for path in files if _is_test_file(path.relative_to(REPO_ROOT).as_posix())}
    test_names = {Path(path).stem.replace(".spec", "").replace(".test", "") for path in test_files}
    missing = []
    for path in files:
        if path.suffix not in CODE_EXTENSIONS or _is_excluded(path):
            continue
        rel_path = path.relative_to(REPO_ROOT).as_posix()
        if _is_test_file(rel_path) or rel_path.startswith("scripts/"):
            continue
        stem = path.stem
        has_match = any(stem in name or name in stem for name in test_names if len(stem) > 5 and len(name) > 5)
        if not has_match:
            missing.append(rel_path)
    return {"test_files": len(test_files), "source_files_without_obvious_test_name_match": len(missing), "examples": missing[:80]}


def _load_previous(path: Path) -> dict[str, object] | None:
    if not path.is_file():
        return None
    try:
        return json.loads(path.read_text())
    except Exception:
        return None


def _delta(current: dict[str, object], previous: dict[str, object] | None) -> dict[str, object]:
    if not previous:
        return {"has_previous": False}
    current_summary = current.get("summary", {})
    previous_summary = previous.get("summary", {})
    current_patterns = current_summary.get("pattern_totals", {}) if isinstance(current_summary, dict) else {}
    previous_patterns = previous_summary.get("pattern_totals", {}) if isinstance(previous_summary, dict) else {}
    return {
        "has_previous": True,
        "previous_generated_at": previous.get("generated_at"),
        "source_lines_delta": current_summary.get("source_lines_analyzed", 0) - previous_summary.get("source_lines_analyzed", 0),
        "source_files_delta": current_summary.get("source_files_analyzed", 0) - previous_summary.get("source_files_analyzed", 0),
        "pattern_deltas": {key: current_patterns.get(key, 0) - previous_patterns.get(key, 0) for key in sorted(set(current_patterns) | set(previous_patterns))},
        "new_top_hotspots": [
            item["path"]
            for item in current.get("top_hotspots_by_score", [])[:20]
            if item.get("path") not in {old.get("path") for old in previous.get("top_hotspots_by_score", [])[:20]}
        ],
    }


def build_report(previous: dict[str, object] | None) -> dict[str, object]:
    files = _git_files()
    metrics = [item for item in (_analyze_file(path) for path in files) if item]
    by_score = sorted(metrics, key=lambda item: item.score, reverse=True)
    by_lines = sorted(metrics, key=lambda item: item.lines, reverse=True)
    scores = [item.score for item in metrics]
    by_ext = Counter(item.ext for item in metrics)
    lines_by_ext: Counter[str] = Counter()
    pattern_totals: Counter[str] = Counter()
    for item in metrics:
        lines_by_ext[item.ext] += item.lines
        for field in PATTERNS:
            pattern_totals[field] += getattr(item, field)

    report: dict[str, object] = {
        "generated_at": _now_iso(),
        "head_sha": _current_sha(),
        "summary": {
            "source_files_analyzed": len(metrics),
            "source_lines_analyzed": sum(item.lines for item in metrics),
            "extensions_by_file_count": dict(by_ext),
            "extensions_by_line_count": dict(lines_by_ext),
            "median_debt_score": median(scores) if scores else 0,
            "pattern_totals": dict(pattern_totals),
        },
        "top_hotspots_by_score": [asdict(item) for item in by_score[:50]],
        "largest_files": [asdict(item) for item in by_lines[:50]],
        "directory_rollup": _directory_rollup(metrics),
        "duplication_fingerprints": _duplication_fingerprints(files),
        "churn_hotspots_6_months": _git_churn(50),
        "test_coverage_proximity": _test_proximity(files),
    }
    report["delta"] = _delta(report, previous)
    return report


def write_markdown(report: dict[str, object], path: Path) -> None:
    summary = report["summary"]
    delta = report.get("delta", {})
    lines = ["# Technical Debt Report", ""]
    lines.append(f"Generated: `{report['generated_at']}`  ")
    lines.append(f"HEAD: `{report['head_sha']}`")
    lines.append("")
    lines.append("## Summary")
    lines.append(f"- Source files analyzed: {summary['source_files_analyzed']:,}")
    lines.append(f"- Source lines analyzed: {summary['source_lines_analyzed']:,}")
    lines.append(f"- Median debt score: {summary['median_debt_score']}")
    if delta.get("has_previous"):
        lines.append(f"- Delta since previous run: {delta['source_files_delta']:+} files, {delta['source_lines_delta']:+} lines")
    else:
        lines.append("- Delta since previous run: first run")
    lines.append("")
    lines.append("## Pattern Totals")
    for key, value in summary["pattern_totals"].items():
        extra = ""
        if delta.get("has_previous"):
            extra = f" ({delta['pattern_deltas'].get(key, 0):+})"
        lines.append(f"- `{key}`: {value:,}{extra}")
    lines.append("")
    lines.append("## Top Hotspots")
    lines.append("| Rank | File | Lines | Score | Complexity | Broad exceptions/catches | Debug logs | Store imports | Suppressions |")
    lines.append("|---:|---|---:|---:|---:|---:|---:|---:|---:|")
    for index, item in enumerate(report["top_hotspots_by_score"][:20], 1):
        lines.append(f"| {index} | `{item['path']}` | {item['lines']:,} | {item['score']} | {item['complexity_signals']} | {item['broad_exception']} | {item['console_log']} | {item['store_internal_import']} | {item['suppression']} |")
    lines.append("")
    lines.append("## Churn Hotspots")
    for index, (file_path, count) in enumerate(report["churn_hotspots_6_months"][:20], 1):
        lines.append(f"- {index}. `{file_path}`: {count} changes in 6 months")
    lines.append("")
    lines.append("## Duplication Signals")
    for item in report["duplication_fingerprints"][:10]:
        files = ", ".join(f"`{file}`" for file in item["files"][:6])
        lines.append(f"- {item['file_count']} files / {item['occurrences']} occurrences: {files}")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate weekly OpenMates technical-debt reports.")
    parser.add_argument("--json-output", default=str(DEFAULT_JSON_REPORT))
    parser.add_argument("--markdown-output", default=str(DEFAULT_MD_REPORT))
    parser.add_argument("--state-file", default=str(DEFAULT_STATE_FILE))
    parser.add_argument("--no-state-update", action="store_true", help="Do not persist this run as the previous-run baseline.")
    args = parser.parse_args()

    state_path = Path(args.state_file)
    previous = _load_previous(state_path)
    report = build_report(previous)

    json_path = Path(args.json_output)
    md_path = Path(args.markdown_output)
    json_path.parent.mkdir(parents=True, exist_ok=True)
    json_path.write_text(json.dumps(report, indent=2) + "\n", encoding="utf-8")
    write_markdown(report, md_path)

    if not args.no_state_update:
        state_path.parent.mkdir(parents=True, exist_ok=True)
        state_path.write_text(json.dumps(report, indent=2) + "\n", encoding="utf-8")

    print(json.dumps({"json_report": str(json_path.relative_to(REPO_ROOT)), "markdown_report": str(md_path.relative_to(REPO_ROOT)), "summary": report["summary"], "delta": report["delta"]}, indent=2))


if __name__ == "__main__":
    main()
