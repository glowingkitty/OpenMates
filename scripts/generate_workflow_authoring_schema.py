#!/usr/bin/env python3
"""Generate the CLI Workflow YAML authoring schema.

The Python YAML compiler is authoritative. This script emits a small TypeScript
schema mirror used by CLI tests and fails in --check mode when the generated
file drifts from the backend contract.

Spec: docs/specs/workflows-cli-runtime/spec.yml
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUTPUT = ROOT / "frontend/packages/openmates-cli/src/generated/workflowAuthoringSchema.ts"


def build_schema() -> dict[str, object]:
    return {
        "version": 1,
        "topLevelFields": ["title", "description", "start_when", "steps", "run_content_retention"],
        "triggers": ["manual", "schedule"],
        "stepForms": [
            "use_app_skill",
            "send_notification",
            "send_chat_message",
            "ask_for_user_input",
            "wait",
            "for_every",
            "repeat_until",
            "if",
        ],
        "retentionModes": ["last_5", "none"],
    }


def render() -> str:
    schema_json = json.dumps(build_schema(), indent=2, sort_keys=True)
    return "// Generated by scripts/generate_workflow_authoring_schema.py. Do not edit.\n" + f"export const workflowAuthoringSchema = {schema_json} as const;\n"


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="Fail if the generated schema is stale")
    args = parser.parse_args()
    content = render()
    if args.check:
        existing = OUTPUT.read_text(encoding="utf-8") if OUTPUT.exists() else ""
        if existing != content:
            print(f"stale: {OUTPUT}")
            return 1
        print(f"ok: {OUTPUT}")
        return 0
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT.write_text(content, encoding="utf-8")
    print(f"wrote: {OUTPUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
