/**
 * CLI PDF Skills E2E Test
 *
 * Tests the zero-knowledge PDF skill pipeline via the CLI:
 *   1. Login to web app and CLI via pair auth
 *   2. Attach a 2-page test PDF in a new chat (web app UI)
 *   3. Wait for upload + OCR processing to complete
 *   4. Capture the PDF embed ID from the chat
 *   5. Ask the AI to read the PDF via chat (triggers pdf/read skill)
 *   6. Verify CLI `chats show --json` shows the AI read result
 *
 * Why this uses CLI instead of REST API:
 *   The PDF skills (read/search/view) resolve file_path → embed_id via
 *   file_path_index injected by the main_processor, and decrypt content
 *   using the user's Vault Transit key stored per-session. REST API calls
 *   without a live session context cannot complete steps 2-4 of the
 *   decryption pipeline. The CLI mirrors the browser's session-based
 *   WebSocket/decrypt flow and can exercise the full pipeline.
 *
 * Fixture: frontend/apps/web_app/tests/fixtures/test_document.pdf
 *   - 2 pages
 *   - Page 1: "OpenMates Test Document" intro text, keyword "encryption"
 *   - Page 2: "Technical Details" with bullet points about architecture
 *   Generated by: backend/tests/fixtures/test_document.pdf (reportlab)
 *
 * Architecture doc: docs/architecture/openmates-cli.md
 *
 * REQUIRED ENV VARS:
 *   - OPENMATES_TEST_ACCOUNT_EMAIL
 *   - OPENMATES_TEST_ACCOUNT_PASSWORD
 *   - OPENMATES_TEST_ACCOUNT_OTP_KEY
 *   - PLAYWRIGHT_TEST_BASE_URL
 *
 * Execution:
 *   npx playwright test frontend/apps/web_app/tests/cli-skills-pdf.spec.ts
 */

