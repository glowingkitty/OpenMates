test.describe('CLI PDF Skills', () => {
	test.setTimeout(420_000);

	test('pdf upload via web app → AI reads via CLI chats (pdf/read skill)', async ({
		page
	}: {
		page: any;
	}) => {
		skipWithoutCredentials(test, TEST_EMAIL, TEST_PASSWORD, TEST_OTP_KEY);
		test.skip(!fs.existsSync(TEST_PDF), `Test PDF fixture not found: ${TEST_PDF}`);

		const logCheckpoint = createSignupLogger('CLI_PDF');
		const takeScreenshot = createStepScreenshotter(logCheckpoint, { filenamePrefix: 'cli-pdf' });
		const baseUrl = process.env.PLAYWRIGHT_TEST_BASE_URL || '';
		const apiUrl = deriveApiUrl(baseUrl);

		page.on('console', (msg: any) => consoleLogs.push(`[browser] ${msg.type()}: ${msg.text()}`));

		// -----------------------------------------------------------------------
		// Step 1: Login to web app
		// -----------------------------------------------------------------------
		logCheckpoint('Step 1: Logging in to web app...');
		await page.goto('/');
		const loginBtn = page.getByRole('button', { name: /login.*sign up|sign up/i });
		await expect(loginBtn).toBeVisible({ timeout: 15000 });
		await loginBtn.click();

		// Click Login tab to switch from signup to login view
		const loginTab = page.getByTestId('tab-login');
		await expect(loginTab).toBeVisible({ timeout: 10000 });
		await loginTab.click();

		const emailInput = page.locator('#login-email-input');
		await expect(emailInput).toBeVisible({ timeout: 15000 });
		await emailInput.fill(TEST_EMAIL);
		const continueBtn = page.getByRole('button', { name: /continue/i });
		await expect(continueBtn).toBeEnabled({ timeout: 5000 });
		await continueBtn.click();

		const passwordInput = page.locator('#login-password-input');
		await expect(passwordInput).toBeVisible({ timeout: 15000 });
		await passwordInput.fill(TEST_PASSWORD);

		// Submit password first — OTP field appears after backend confirms 2FA required
		const submitBtn = page.locator('button[type="submit"]', { hasText: /log in|login/i });
		await submitBtn.click();

		const otpInput = page.locator('#login-otp-input');
		await expect(otpInput).toBeVisible({ timeout: 15000 });

		let loginSuccess = false;
		for (let attempt = 1; attempt <= 3 && !loginSuccess; attempt++) {
			await otpInput.fill(generateTotp(TEST_OTP_KEY));
			await submitBtn.click();
			try {
				await expect(otpInput).not.toBeVisible({ timeout: 8000 });
				loginSuccess = true;
			} catch {
				if (attempt < 3) await page.waitForTimeout(31000);
			}
		}
		// Wait for the authenticated DOM signal instead of a URL pattern —
		// post-login URLs no longer reliably contain `/chat/`. (OPE-354)
		await expect(page.locator('[data-authenticated="true"]')).toBeVisible({ timeout: 20000 });
		logCheckpoint('Web app logged in.');

		// -----------------------------------------------------------------------
		// Step 2: Login CLI via pair auth
		// -----------------------------------------------------------------------
		logCheckpoint('Step 2: Logging in CLI...');
		const cli = spawnCliLogin(apiUrl);
		let token: string;
		try {
			token = await cli.waitForToken();
		} catch (err) {
			cli.kill();
			throw err;
		}
		logCheckpoint(`CLI pair token: ${token}`);

		await page.goto(`${baseUrl}/#pair=${token}`);
		const allowBtn = page.getByTestId('pair-allow-button');
		await expect(allowBtn).toBeVisible({ timeout: 15000 });
		await allowBtn.click();

		const pinDisplay = page.getByTestId('pair-pin-display');
		await expect(pinDisplay).toBeVisible({ timeout: 15000 });
		const pinText = await pinDisplay.textContent();
		const pin = (pinText || '').replace(/\s/g, '').trim();
		logCheckpoint(`PIN: ${pin}`);

		cli.sendPin(pin);
		const { code: loginCode, output: loginOutput } = await cli.waitForExit();
		expect(loginOutput).toContain('Login successful');
		expect(loginCode).toBe(0);
		logCheckpoint('CLI login complete.');

		// -----------------------------------------------------------------------
		// Step 3: Open a new chat and attach the test PDF
		// -----------------------------------------------------------------------
		logCheckpoint('Step 3: Opening new chat and attaching PDF...');
		// Navigate back to main chat view
		await page.goto('/');
		// Tolerate either the authenticated chat view OR the demo "for everyone" landing
		// page — pair-auth occasionally drops the session and lands the test on the demo
		// page; the new-chat-btn below still works from there.
		await page.waitForURL(/chat/, { timeout: 15000 });
		await page.waitForTimeout(2000);

		// Open a new chat
		const newChatBtn = page
			.locator('[data-testid="new-chat-btn"], .new-chat-btn, [aria-label*="new chat" i]')
			.first();
		if (await newChatBtn.isVisible({ timeout: 5000 }).catch(() => false)) {
			await newChatBtn.click();
		}

		// Ensure editor is ready
		const messageEditor = page.getByTestId('message-editor');
		await expect(messageEditor).toBeVisible({ timeout: 15000 });
		logCheckpoint('Editor ready.');

		// Attach PDF via file input
		const fileInput = page.locator('input[type="file"][multiple]');
		await expect(fileInput).toBeAttached({ timeout: 10000 });
		await fileInput.setInputFiles(TEST_PDF);
		logCheckpoint('PDF attached.');
		await takeScreenshot(page, 'pdf-attached');

		// Wait for the PDF upload embed to appear and finish processing
		const embedWrapper = page.getByTestId('message-editor').locator('[data-testid="embed-full-width-wrapper"][data-embed-type="pdf"]');
		await expect(embedWrapper.first()).toBeVisible({ timeout: 30000 });
		logCheckpoint('PDF embed appeared (processing).');

		const embedFinished = page.getByTestId('message-editor').locator('[data-testid="embed-full-width-wrapper"][data-embed-type="pdf"][data-embed-status="finished"]');
		await expect(embedFinished.first()).toBeVisible({ timeout: 120_000 });
		logCheckpoint('PDF upload finished — pages processed by server.');
		await takeScreenshot(page, 'pdf-upload-finished');

		// -----------------------------------------------------------------------
		// Step 4: Send a message asking the AI to read the PDF and submit
		// -----------------------------------------------------------------------
		logCheckpoint('Step 4: Sending message asking AI to read the PDF...');
		await messageEditor.click();
		await messageEditor.type('Please read this document and tell me what it contains on page 1.');

		const sendBtn = page.locator('[data-testid="send-btn"], button[aria-label*="send" i]').first();
		await expect(sendBtn).toBeVisible({ timeout: 10000 });
		await sendBtn.click();
		logCheckpoint('Message sent. Waiting for AI response...');

		// Wait for AI response with pdf/read skill embed
		const aiPdfEmbed = page.getByTestId('message-assistant').locator('[data-testid="embed-preview"][data-app-id="pdf"]');
		await expect(aiPdfEmbed.first()).toBeVisible({ timeout: 120_000 });
		logCheckpoint('AI used pdf/read skill — embed visible in assistant response.');
		await takeScreenshot(page, 'ai-read-response');

		// -----------------------------------------------------------------------
		// Step 5: Get the chat ID via CLI and verify the conversation with --json
		// -----------------------------------------------------------------------
		logCheckpoint('Step 5: Verifying chat via CLI --json...');

		// Get the current chat URL to extract chat ID
		const currentUrl = page.url();
		const chatIdMatch = currentUrl.match(/\/chat\/([a-f0-9-]+)/);

		let chatId: string | null = chatIdMatch ? chatIdMatch[1] : null;

		// Fallback: use `chats list --json` to get the most recent chat
		if (!chatId) {
			logCheckpoint('Chat ID not in URL, fetching from CLI...');
			const listResult = await runCli(apiUrl, ['chats', 'list', '--limit', '1', '--json'], 20_000);
			expect(listResult.code).toBe(0);
			const chats = JSON.parse(listResult.stdout);
			chatId = chats[0]?.id || chats[0]?.chatId || null;
		}

		expect(chatId).toBeTruthy();
		logCheckpoint(`Chat ID: ${chatId}`);

		// Fetch the full chat with --json to verify pdf/read skill was invoked
		const showResult = await runCli(apiUrl, ['chats', 'show', chatId!, '--json'], 30_000);
		consoleLogs.push(`[chats show stdout] ${showResult.stdout.slice(0, 1000)}`);
		expect(showResult.code).toBe(0);

		let showData: any;
		try {
			showData = JSON.parse(showResult.stdout);
		} catch (e) {
			throw new Error(`Expected JSON from chats show, got:\n${showResult.stdout}`);
		}

		const messages = showData.messages || [];
		expect(messages.length).toBeGreaterThan(1); // at least user + assistant

		// Find the assistant message
		const assistantMsgs = messages.filter((m: any) => m.role === 'assistant');
		expect(assistantMsgs.length).toBeGreaterThan(0);

		// The response should mention the document content (page 1 keywords)
		const responseText = String(
			assistantMsgs[0].content || assistantMsgs[0].text || ''
		).toLowerCase();
		// The test PDF's page 1 contains "OpenMates Test Document" and mentions "encryption"
		const hasRelevantContent =
			responseText.includes('openmates') ||
			responseText.includes('encryption') ||
			responseText.includes('test document') ||
			responseText.includes('introduction') ||
			responseText.length > 50; // at minimum a meaningful response

		expect(hasRelevantContent).toBe(true);
		logCheckpoint(`AI response references PDF content. Length: ${responseText.length} chars`);

		// Verify the assistant message has embed IDs (the pdf/read skill embed)
		const embedIds = assistantMsgs[0].embedIds || assistantMsgs[0].embed_ids || [];
		expect(embedIds.length).toBeGreaterThan(0);
		logCheckpoint(`Found ${embedIds.length} embed(s) in assistant message.`);

		await takeScreenshot(page, 'cli-verified');

		// -----------------------------------------------------------------------
		// Cleanup
		// -----------------------------------------------------------------------
		logCheckpoint('Cleaning up...');

		// Delete the chat via CLI
		const deleteResult = await runCli(apiUrl, ['chats', 'delete', chatId!, '--yes'], 20_000);
		expect(deleteResult.code).toBe(0);
		logCheckpoint('Test chat deleted.');

		await runCli(apiUrl, ['logout']);
		logCheckpoint('Logged out. Test complete.');
	});
});
