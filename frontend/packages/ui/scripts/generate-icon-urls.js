// frontend/packages/ui/scripts/generate-icon-urls.js
//
// Build script to auto-generate CSS custom properties for all SVG icons.
// Scans static/icons/*.svg and produces icon-urls.generated.css with a
// :root block mapping every SVG file to a --icon-url-{name} variable.
//
// This eliminates the need to manually register icons in CSS or component
// files — any SVG dropped into static/icons/ is automatically available
// as var(--icon-url-{filename}) across the entire frontend.
//
// **Usage**: Run automatically via the "prepare" / "prebuild" npm scripts.

import { readdirSync, writeFileSync, existsSync } from "fs";
import { join, dirname, resolve, basename } from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Paths
const ICONS_DIR = resolve(__dirname, "../static/icons");
const OUTPUT_FILE = resolve(__dirname, "../src/styles/icon-urls.generated.css");

function generateIconUrls() {
  if (!existsSync(ICONS_DIR)) {
    console.error(
      `[generate-icon-urls] Icons directory not found: ${ICONS_DIR}`,
    );
    process.exit(1);
  }

  // Read all SVG files and sort alphabetically for deterministic output
  const svgFiles = readdirSync(ICONS_DIR)
    .filter((f) => f.endsWith(".svg"))
    .sort();

  if (svgFiles.length === 0) {
    console.warn("[generate-icon-urls] No SVG files found in", ICONS_DIR);
    return;
  }

  // Convert filename to CSS variable name:
  // "google-authenticator.svg" → "google-authenticator"
  // "3dmodels.svg" → "3dmodels"
  // "record_video.svg" → "record_video"
  const entries = svgFiles.map((file) => {
    const name = basename(file, ".svg");
    return `  --icon-url-${name}: url('@openmates/ui/static/icons/${file}');`;
  });

  const css = `/* AUTO-GENERATED — do not edit manually.
 * Generated by: scripts/generate-icon-urls.js
 * Source: static/icons/*.svg (${svgFiles.length} icons)
 *
 * Provides --icon-url-{name} CSS custom properties for every SVG icon.
 * Usage: background-image: var(--icon-url-chat);
 *        mask-image: var(--icon-url-lock);
 */
:root {
${entries.join("\n")}
}
`;

  writeFileSync(OUTPUT_FILE, css, "utf-8");
  console.log(
    `[generate-icon-urls] Generated ${svgFiles.length} icon URL variables → ${OUTPUT_FILE}`,
  );
}

generateIconUrls();
